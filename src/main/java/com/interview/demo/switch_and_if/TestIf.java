import java.util.Scanner;

public class TestIf {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String next = scanner.next();
        if ("a".equals(next)) {
            System.out.println(next);

        }
        if ("b".equals(next)) {
            System.out.println(next);
        }
    }
}
